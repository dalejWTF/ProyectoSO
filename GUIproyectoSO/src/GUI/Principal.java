    private void btnGenerarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerarActionPerformed
        // TODO add your handling code here:
        n = Integer.parseInt(this.txtCantidad.getText());
        this.txtRafagaCPU.setEnabled(true);
        this.txtTiempoLlegada.setEnabled(true);
        this.btnAgregar.setEnabled(true);
        this.lblNroProceso.setText(String.format("Proceso %d", i + 1));

    }//GEN-LAST:event_btnGenerarActionPerformed

    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) this.jtbProcesos.getModel();

        if (i < n) {
            if (this.txtRafagaCPU.getText() != null && this.txtTiempoLlegada != null) {
                Proceso proceso = new Proceso(
                        String.format("P%d", i), Integer.parseInt(this.txtRafagaCPU.getText()), Integer.parseInt(this.txtTiempoLlegada.getText())
                );
                
                procesos.add(proceso);
                i++;
                Vector v= new Vector();
                v.add(String.format("P%d", i));
                v.add(Integer.parseInt(this.txtTiempoLlegada.getText()));
                v.add(Integer.parseInt(this.txtRafagaCPU.getText()));
                model.addRow(v);
                this.lblNroProceso.setText(String.format("Proceso %d", i + 1));
                Limpiar();
                if (i == n) {
                    JOptionPane.showMessageDialog(null, "Todos los datos han sido registrados!");
                    
                    this.btnAgregar.setEnabled(false);
                    this.btnCalcular.setEnabled(true);
                    this.btnBorrar.setEnabled(true);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Datos ingresados vacios");
            }
        }

    }//GEN-LAST:event_btnAgregarActionPerformed

